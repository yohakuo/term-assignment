x=[-5.0,-3.0,1.0,2.0,2.5,3.0,5.0];
y=[];                             %建立存放所有y值的矩阵
for x0=x
    if x0<0&&x0~=-3
        y=[y,x0*x0+x0-6];
    elseif x0>=0&&x0<5&&x0~=2&&x0~=3
        y=[y,x0*x0-5*x0+6];
    else
        y=[y,x0*x0-x0-1];
    end
end
x                                 %输出所有x
y                                 %输出所有y